def decrypt(char: int) -> str:
    return (chr((char ^ 0xffffffab) + 0xc & 0xff))

passwd: list = [0x2fe, 0x2f7, 0x2fe, 0x2f7, 0x2f8, 0x2c9, 0x2c8, 0x2fd, 0x2c8, 0x2f3, 0x2c6, 0x2fc, 0x2fe, 0x2c9, 0x2fc, 0x2cd, 0x2fe, 0x2fc, 0x2f4, 0x2ca, 0x2f2]
decrypted: str = ""

for byte in passwd:
    decrypted += decrypt(byte)

print(decrypted)
